#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*new;
	unsigned int	i;

	if (!s1)
		return (NULL);
	new = (char *)malloc(sizeof(*new) * (ft_strlen(s1) + 1));
	if (!new)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
